/*============================================================================
static bool sensor_node_is_busy(void)
------------------------------------------------------------------------------
Purpose: check whether key node is busy
Input  : key id
Output : True, busy; false, not idle
Notes  : none
============================================================================*/
static bool sensor_node_is_busy(uint8_t k)
{
  const uint8_t num_key_sensors = tslib_num_key_sensors();
  uint8_t state;

  if (k < num_key_sensors) {
    state = tslib_get_sensor_state(k);
    return (state == QTM_KEY_STATE_CAL) || (state == QTM_KEY_STATE_FILT_IN) || (state == QTM_KEY_STATE_FILT_OUT) ;
  }

  return 0;
}
/*============================================================================
touch_ret_t touch_disable_nonlp_sensors(void)
------------------------------------------------------------------------------
Purpose: Disable
Input  : None
Output : touch_ret_t
Notes  : none
============================================================================*/
static void soft_enable_nonlp_sensors(uint8_t key_mask, bool en)
{
  const uint8_t num_key_sensors = tslib_num_key_sensors();
  uint8_t state;


  for (uint8_t k = 0; k < num_key_sensors; k++) {
    state = tslib_get_sensor_state(k);
    
    if (TEST_BIT(key_mask, k)) {
      if (state == QTM_KEY_STATE_SUSPEND) {
        tslib_key_resume(k);
      }
    } else {
      if (en) {
        if (state == QTM_KEY_STATE_SUSPEND) {
          tslib_key_resume(k);
        }
      } else {
        if (state != QTM_KEY_STATE_SUSPEND) {
          tslib_key_suspend(k);
        }
      }
    }
  }
}

/*============================================================================
static touch_ret_t touch_autoscan_sensor_node_soft(void)
------------------------------------------------------------------------------
Purpose: Configures the PTC for sleep mode measurement of a current sleep node
Input  : none
Output : touch_ret_t
Notes  : none
============================================================================*/
static touch_ret_t touch_autoscan_sensor_node_soft(void) 
{
  const uint8_t num_sensors = tslib_num_key_sensors();
  const uint8_t mask = get_autoscan_sensor_node_mask();
  uint8_t next, key = current_autoscan_sensor_soft;
  
  // Check current key whether busy
  if (key < num_sensors) {
    if (sensor_node_is_busy(key)) {
      return TOUCH_ACQ_INCOMPLETE;
    }
  }

  // Select next low power sensor
  for (uint8_t cnt = key + 1; cnt <= key + num_sensors; cnt++) {
    next = cnt;
    if (next >= num_sensors) {
      next -= num_sensors;
    }
    
    if (TEST_BIT(mask, next)) {
      key = next;
      break;
    }
  }
  
  if (key >= num_sensors) {
    return TOUCH_INVALID_INPUT_PARAM;
  }

  // Disable non-autoscan sensor
  soft_enable_nonlp_sensors(BIT8(key), false);
  
  // Update the autoscan sensor
  current_autoscan_sensor_soft = key;

  return TOUCH_SUCCESS;
}

